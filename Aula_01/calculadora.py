#%%

print("1+1 =",1+1) # essa é uma operação de adição

print("10-5 =",10-5) # essa é uma operação de subtração

# %%
print("10*2 =",10*2) # essa é uma operação de multiplicação

print("10/3 =",10/3) # essa é uma operação de divisão

print("10 // 3 =",10 // 3) # essa é uma operação de divisão inteira

print("10 % 4 =",10 % 4) # essa é uma operação de resto da divisão

#%%

print ('3  ** 4 =',3 ** 4) # essa é uma operação de potência

